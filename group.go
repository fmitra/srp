/*
Group represents constant parameters in the SRP protocol.

It is a combination of large prime value, N and a primative root
of N, known as G, used by both the client and server.

RFC 5054 Section 3.2 warns that due to the difficulty of calculating
eligible prime values in real time, implementations of SRP should
use pre-defined values from a trusted source. The values provided
in this package come from RFC 5054 Appendix A.

Ref:

https://tools.ietf.org/html/rfc5054#section-3.2
https://tools.ietf.org/html/rfc5054#appendix-A
*/
type Group struct {
	// Hex is the hexadecimal value for prime N
	Hex string
	// G is a primative root of prime N
	G *big.Int
	// N is a large prime value
	N *big.Int
}

// CalcN returns the integer value of a Group's Hex value.
func (g *Group) CalcN() (*big.Int, error) {
	N, ok := new(big.Int).SetString(g.Hex, 0)
	if !ok {
		return big.NewInt(0), ErrInvalidPrime
	}

	g.N = N
	return N, nil
}

// NewGroup returns a new Group. It expects a string parameter
// in the format of <primative_root>:<prime_as_hexadecimal_string>
// or <G>:<Hex>
func NewGroup(s string) (*Group, error) {
	v := strings.Split(s, ":")
	hex := v[1]
	g, ok := new(big.Int).SetString(v[0], 10)
	if !ok {
		return &Group{}, ErrInvalidPrimativeRoot
	}

	group := &Group{
		Hex: hex,
		G:   g,
	}
	group.CalcN()
	return group, nil
}
